package edu.eci.arsw.synchdrive.model;

public class Application {

    private Integer id;
    private String name;

    public Application() {

    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }
}